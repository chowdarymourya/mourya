package javaprogrames;

public class loopinsideloop {

	public static void main(String[] args) {
		int year=1;
		int months=12;
		
	for(int i=1;i<=year;i++)
	{
		System.out.println("number of years"+i);//
	}
	for(int m=1;m<=months;m++)
	{
		System.out.println("number of months"+m);
	}
 
	}

}
